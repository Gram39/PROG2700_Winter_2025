/*
In this example:
    We define a function named greetUser that takes one parameter, name.
    The function returns a greeting message that includes the provided name.
    We call the greetUser function with the argument "Alice" and store the result in the greetingMessage variable.
    Finally, we log the greetingMessage to the console.
*/

// Define a function to greet a user
function greetUser(name) {
    return "Hello, " + name + "!";
}

// Call the function and store the result
let greetingMessage = greetUser("Alice");

// Log the result to the console
console.log(greetingMessage);


