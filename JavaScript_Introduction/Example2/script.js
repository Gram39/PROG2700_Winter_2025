// script.js

/*
This script demonstrates the following variable types:
    String: A sequence of characters.
    Number: Numeric values.
    Boolean: True or false values.
    Array: A collection of values.
    Object: A collection of key-value pairs.
    Null: An intentional absence of any object value.
    Undefined: A variable that has been declared but not assigned a value.
    Symbol: A unique and immutable primitive value.
    Each variable is logged to the console with a descriptive label.
*/

// String
let greeting = "Hello, World!";
console.log("String:", greeting);

// Number
let age = 25;
console.log("Number:", age);

// Boolean
let isStudent = true;
console.log("Boolean:", isStudent);

// Array
let colors = ["red", "green", "blue"];
console.log("Array:", colors);

// Object
let person = {
    firstName: "John",
    lastName: "Doe",
    age: 30
};
console.log("Object:", person);

// Null
let emptyValue = null;
console.log("Null:", emptyValue);

// Undefined
let notAssigned;
console.log("Undefined:", notAssigned);

// Symbol
let uniqueId = Symbol("id");
console.log("Symbol:", uniqueId);


